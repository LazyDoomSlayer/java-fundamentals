package umcs.lesson01;

// Task Description: Declare variables of each basic type (int, double, boolean, String) and assign values to them. Then print them to the console

public class Ex03_BasicTypes {
    static void main(String[] args) {
        int age = 45;
        double height = 2.31;
        boolean isStudent = false;
        String name = "Shaq";

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + " meters");
        System.out.println("Is student: " + isStudent);
    }
}

