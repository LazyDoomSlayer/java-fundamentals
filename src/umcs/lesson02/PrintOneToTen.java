/*
 * Exercise 4 â€” Print Numbers from 1 to 10 (While Loop)
 * ----------------------------------------------------
 * This program prints numbers from 1 to 10 using a while loop.
 *
 * Demonstrates:
 *  - loop initialization and condition checking
 *  - incrementing a counter variable
 *  - basic use of while loop in Java
 */

public class PrintOneToTen {
     static void main(String[] args) {
        int i = 1;

        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}
